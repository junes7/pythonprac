# 10진수와 2진수 변환하기
bin(13) # 10진수 13을 2진수로 변환
0b1101	# 2진수 1101을 10진수로 변환
int('1101', 2)    # 2진수로 된 문자열 1101을 10진수로 변환

# 비트 논리 연산자 사용하기
bin(0b1101 & 0b1001)    # 비트 AND
13 & 9                  # 비트 AND
bin(0b1101 | 0b1001)    # 비트 OR
13 | 9                  # 비트 OR
bin(0b1101 ^ 0b1001)    # 비트 XOR
13 ^ 9                  # 비트 XOR
bin(~0b1101)            # 비트 NOT
~13                     # 비트 NOT







